package com.prodevans.zeno.pojo;

public class TopUp 
{

	private String planSize;
	private String planName;
	private double amount;
	private String postFUP;
	
	
	
	public String getPostFUP() {
		return postFUP;
	}
	public void setPostFUP(String postFUP) {
		this.postFUP = postFUP;
	}
	public String getPlanSize() {
		return planSize;
	}
	public void setPlanSize(String planSize) {
		this.planSize = planSize;
	}
	public String getPlanName() {
		return planName;
	}
	public void setPlanName(String planName) {
		this.planName = planName;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	
}
